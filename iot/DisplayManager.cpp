void DisplayManager::drawTextOverlay(int bpm, int spo2, float rmssd, float sdrr, int pnn50, String statusText) {
	
	// 1. Teks BPM
	display.setTextSize(2);
	display.setTextColor(SSD1306_BLACK);
	int16_t x_center = (128 - (String(bpm).length() * 12)) / 2;
	display.setCursor(x_center, 25);
	display.print(bpm);

	// 2. Teks SpO2
	display.setTextSize(1);
	display.setTextColor(SSD1306_WHITE);
    
	// SpO2 Label
	display.setCursor(3, 0); 
	display.print("SpO2");
	// SpO2 Nilai
	display.setCursor(3, 10); 
	display.print(spo2); display.print("%");

	// 3. Teks RMSSD
	display.setCursor(3, 22);
	display.print("RMSSD");
	display.setCursor(3, 32);
	display.print(rmssd, 1);

	// 4. Teks SDRR
	display.setCursor(95, 0);
	display.print("SDRR");
	display.setCursor(95, 10);
	display.print(sdrr, 1);

	// 5. Teks pNN50 
	display.setCursor(95, 22);
	display.print("pNN50");
	display.setCursor(95, 32);
	display.print(pnn50); display.print("%");

	// 6. Status
	int x_pos = (statusText.length() > 6) ? 40 : 47;
	display.setCursor(x_pos, 54);
	display.setTextColor(SSD1306_WHITE);
	display.print(statusText);

	display.fillRect(0, 62, 128, 8, SSD1306_BLACK);
}

void DisplayManager::updateDisplay(int bpm, int spo2, float rmssd, float sdrr, int pnn50, String status, bool fingerDetected, bool frozen) {
	if (millis() - lastFrameTime > 100) {
		lastFrameTime = millis();

		currentFrame++;
		if (currentFrame > 4) currentFrame = 0;

		if (!fingerDetected) {
			showNoFinger();
			return;
		}

		display.clearDisplay();
		display.drawBitmap(0, 0, heart_frameallArray[currentFrame], 128, 64, 1);
		drawTextOverlay(
			frozen ? bpm : (bpm > 30 ? bpm : 0), 
			spo2, 
			rmssd, 
			sdrr, 
			pnn50, 
			status
		);
		display.display();
	}
}

