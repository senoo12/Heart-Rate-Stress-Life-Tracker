void DisplayManager::init() {
	if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
		// Handle error
		while (1);
	}
	display.clearDisplay();
	display.setTextColor(SSD1306_WHITE);
	display.setTextSize(1);
	display.display();

	// Intro
	showAnimationIntro();
}

void DisplayManager::showMessage(	String message) {
	display.clearDisplay();
	display.setCursor(0, 0);
	display.println(message);
	display.display();
}

void DisplayManager::showNoFinger() {
	display.clearDisplay();
	display.setTextColor(SSD1306_WHITE);
	display.setCursor(30, 15);
	display.setTextSize(2);
	display.println("TEMPEL");
	display.setCursor(40, 35);
	display.setTextSize(2);
	display.println("JARI");
	display.display();
}

void DisplayManager::showAnimationIntro() {
	for (int k = 0; k < 3; k++) {
		for (int i = 0; i < 5; i++) {
			display.clearDisplay();
			display.drawBitmap(0, 0, heart_frameallArray[i], 128, 64, 1);
			display.fillRect(0, 62, 128, 8, SSD1306_BLACK);
			display.display();
			delay(100);
		}
	}
}

